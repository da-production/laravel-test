    public function like(Request $request){
        $like = Like::where(
            [
                ['user_id','=',$request->user],
                ['post_id','=',$request->post]
            ]
        );
        if($like->first() != null){
            $like->delete();
            return response()
            ->json(['like' => 'disliked']);
        }else{
            Like::create([
                'post_id' => $request->post,
                'user_id' =>   $request->user
            ]);
            return response()
            ->json(['like' => 'liked']);
        }
    }

